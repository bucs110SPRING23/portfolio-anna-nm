from Rectangle import Rectangle

class Surface: 
    def __init__(self, filename, x, y, h, w): 
        ''' 
        self.rect: create a rectangle using the Rectangle() function from Rectangle.py
        self.image: save the filename as a string
        '''
        self.filename = Rectangle
        self.rect = Rectangle(x, y, h, w)
        self.image = str(filename)
    def getRect(self): 
        '''
        function: return the attribute of the self.rect rectangle
        '''
        return self.rect
